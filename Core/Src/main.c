/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM4_Init();
  MX_TIM5_Init();
  MX_TIM3_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */

  float position=0;
  uint8_t dir=0;
  volatile float increment=0.01;
  uint16_t potMax=TIM4->ARR*0.26;
  uint16_t potMin=TIM4->ARR*0.05;
  volatile uint8_t allowTurn=0;


  //for laser
  uint8_t byteData, sensorState=0;
  uint16_t wordData;
  uint8_t ToFSensor = 1; // 0=Left, 1=Center(default), 2=Right
  uint16_t Distance;
  uint16_t SignalRate;
  uint16_t AmbientRate;
  uint16_t SpadNum;
  uint8_t RangeStatus;
  uint8_t dataReady;

  HAL_TIM_Base_Init(&htim3);
  HAL_TIM_PWM_Init(&htim4);
  HAL_TIM_Base_Start(&htim3);

  HAL_TIM_PWM_Start(&htim4, TIM_CHANNEL_4);



  HAL_I2C_Init(&hi2c1);


  pLaserI2Chandle=&hi2c1;
  volatile uint8_t bootState=0;
  VL53L1X_BootState(dev, bootState);

  status = VL53L1_RdByte(dev, 0x010F, &byteData);

    status = VL53L1_RdByte(dev, 0x0110, &byteData);
    status = VL53L1_RdWord(dev, 0x010F, &wordData);
    while(sensorState==0){
  		status = VL53L1X_BootState(dev, &sensorState);
  	HAL_Delay(2);
    }


  status = VL53L1X_SensorInit(dev);
  status = VL53L1X_SetDistanceMode(dev, 2); /* 1=short, 2=long */
  status = VL53L1X_SetTimingBudgetInMs(dev, 100); /* in ms possible values [20, 50, 100, 200, 500] */
  status = VL53L1X_SetInterMeasurementInMs(dev, 100); /* in ms, IM must be > = TB */

  status = VL53L1X_StartRanging(dev);



  /* USER CODE END 2 */
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
	  //startIdleTimer(idleTime);

	  switch (allowTurn) {
	  case 0:
		  if(position!=0){
			  position=0;
			  TIM4->CCR4=potMin;
		  }
		  break;
	  default:

		  switch (dir) {
		  case 0:
			  if(position>=1-increment){
				  dir=1;
				  position=1;
			  }
			  else{
				  position+=increment;
			  }

			  break;
		  case 1:
			  if(position<=increment){
				  dir=0;
				  position=0;
			  }
			  else{
				  position-=increment;
			  }

			  break;
		  }

		  TIM4->CCR4=interpolate(position,potMin,potMax);

		  break;
	  }
	  while (dataReady == 0){
	  		  status = VL53L1X_CheckForDataReady(dev, &dataReady);
	  		  HAL_Delay(2);
	  	  }
	  	  dataReady = 0;
	  	  status = VL53L1X_GetRangeStatus(dev, &RangeStatus);
	  	  status = VL53L1X_GetDistance(dev, &Distance);
	  	  status = VL53L1X_GetSignalRate(dev, &SignalRate);
	  	  status = VL53L1X_GetAmbientRate(dev, &AmbientRate);
	  	  status = VL53L1X_GetSpadNb(dev, &SpadNum);
	  	  status = VL53L1X_ClearInterrupt(dev); /* clear interrupt has to be called to enable next interrupt*/
	  	  printf("%u, %u, %u, %u, %u\n", RangeStatus, Distance, SignalRate, AmbientRate,SpadNum);


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

